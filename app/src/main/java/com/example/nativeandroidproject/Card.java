package com.example.nativeandroidproject;

public class Card {

    private String imageUrl;
    private String title;

    public Card(String imageUrl, String title) {
        this.imageUrl = imageUrl;
        this.title = title;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public String getTitle() {
        return title;
    }




}
